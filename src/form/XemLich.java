    public void khoanhVung()
    {
        Iterator<GiangVien> myIterator = listGiangVien_loc.iterator();
          
           while(myIterator.hasNext())
           {
               GiangVien gv = new GiangVien();
               gv = myIterator.next();
                   if(listFilter.getSelectedValuesList().isEmpty())
                   {
                   if(gv.getListDangKyPhong().get(0).getMaPhong().compareTo((String)comboFilterClass.getSelectedItem()) != 0)
                     {
                       myIterator.remove();
                      }
                   }
                   else
                   {
                    if(gv.getListDangKyPhong().get(0).getMaPhong().compareTo((String)comboFilterClass.getSelectedItem()) != 0)
                      {
                       myIterator.remove();
                       }
                    else
                    {
                        if(!listFilter.getSelectedValuesList().contains(gv.getListDangKyPhong().get(0).getThoiGian().toString()))
                        {
                            myIterator.remove();
                        }
                    
                   }
            }
    }
    }
    
    
