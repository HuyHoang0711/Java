public void sapXep()
    {
       Comparator<GiangVien> c = new Comparator<GiangVien>()
       {
         @Override
         public int compare(GiangVien gv1, GiangVien gv2)
         {
             if(gv1.getListDangKyPhong().get(0).getMaPhong().compareToIgnoreCase
                        (gv2.getListDangKyPhong().get(0).getMaPhong()) == 0)
             {
                 return gv1.getListDangKyPhong().get(0).getThoiGian().compareTo
                           (gv2.getListDangKyPhong().get(0).getThoiGian());
             }
             else
             return gv1.getListDangKyPhong().get(0).getMaPhong().compareToIgnoreCase
                        (gv2.getListDangKyPhong().get(0).getMaPhong()); 
         }

       };
       if(String.valueOf(comboSapXep.getSelectedItem()).equals("ASC"))
           Collections.sort(listGiangVien_loc, c);
       else
       {
           Collections.sort(listGiangVien_loc, c);
           Collections.reverse(listGiangVien_loc);
       }
       }
