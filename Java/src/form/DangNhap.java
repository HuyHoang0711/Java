    public void loadForm()
    {
        this.dispose();
        if(btAdmin.isSelected())
        {
            new GiaoDienQT().setVisible(true);
            quyenDangNhap = "admin";
        }
        else
        {
         new GiaoDienGV().setVisible(true);
         quyenDangNhap = "giangvien";
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jButton1.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        try{
        if(kiemTraTrong())
        {
            
            if(kiemTraHopLe())
            {
                jButton1.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
                JOptionPane.showMessageDialog(null, "Chào mừng " + this.tenNhanVien, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadForm();
            }
            else
            {
                jButton1.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
                 jLabel5.setText("Tên đăng nhập hoặc mật khẩu không chính xác!");
            }         
        }
        }
        catch(KiemTraDuLieu ex)
        {
             JOptionPane.showMessageDialog(null, ex.getMessage(), "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

