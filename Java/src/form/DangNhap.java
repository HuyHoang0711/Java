    public boolean kiemTraTrong() throws KiemTraDuLieu
    {
        if(txtTaiKhoan.getText().equals(""))
        {
            txtTaiKhoan.requestFocus();
            throw new KiemTraDuLieu("Tài khoản không được để trống!");
        }
        if(String.valueOf(txtMatKhau.getPassword()).equals(""))
        {
            txtMatKhau.requestFocus();
             throw new KiemTraDuLieu("Mật khẩu không được để trống!");
        }
        if(!txtTaiKhoan.getText().matches("\\w+"))
       {
           txtTaiKhoan.requestFocus();
           throw new KiemTraDuLieu("Tài khoản không gồm kí tự đặc biệt!");
       }   
        if(!String.valueOf(txtMatKhau.getPassword()).matches("\\w+"))
        {
            txtMatKhau.requestFocus();
            throw new KiemTraDuLieu("Mật khẩu không gồm kí tự đặc biệt!");
        }
        return true;
    }
    public boolean kiemTraHopLe()
    {
        String tableName = "";
        String columnName = "";
        if(btAdmin.isSelected())
        {
            tableName = "NGUOIQUANTRI";
            columnName = "MaNhanVien";
        }
        if(btGiangVien.isSelected())
        {
            tableName = "GIANGVIEN";
            columnName = "MaGiangVien";
        }
         String sql = "SELECT HoTen, "+columnName+" FROM "+tableName+" WHERE TaiKhoan = '"+txtTaiKhoan.getText()
                          +"' AND MatKhau = '"+String.valueOf(txtMatKhau.getPassword())+"'";
        try{
        ResultSet rs = Database.getData(sql);
            while(rs.next())
            {
              maNhanVien = rs.getString(columnName);
              tenNhanVien = rs.getString("HoTen");
              return true;
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Tải dữ liệu từ CSDL thất bại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
