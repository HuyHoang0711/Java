    private void toggleCongTacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_toggleCongTacActionPerformed
        // TODO add your handling code here:
        if(toggleCongTac.getText().equals("OFF"))
        {
        toggleCongTac.setText("ON");
        txtMatKhauCu.setEditable(true);
        txtMatKhauMoi.setEditable(true);
        txtXacNhan.setEditable(true);
        txtMatKhauCu.requestFocus();
        return;
        }
        if(toggleCongTac.getText().equals("ON"))
        {
            toggleCongTac.setText("OFF");
            
            khoiTaoMatKhau();
        }
    }//GEN-LAST:event_toggleCongTacActionPerformed

