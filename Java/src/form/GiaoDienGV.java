    public void thongTinGiangVien()
    {
        String sql = "SELECT * FROM GIANGVIEN WHERE MaGiangVien = '"+this.maGiangVien+"'";
        try{
            ResultSet rs = Database.getData(sql);
            while(rs.next())
            {
            gv.setMaNhanVien(rs.getString("MaGiangVien"));
            gv.setHoTen(rs.getString("HoTen"));
            gv.getTaiKhoanGV().setTenDangNhap(rs.getString("TaiKhoan"));
            gv.getTaiKhoanGV().setMatKhau(rs.getString("MatKhau"));
            gv.setGioiTinh(rs.getString("GioiTinh"));
            gv.setSoDienThoai(rs.getString("SoDienThoai"));
            gv.setGiangVienNganh(rs.getString("GiangVienNganh"));
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
             JOptionPane.showMessageDialog(null, "Tải dữ liệu từ CSDL thất bại!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }
