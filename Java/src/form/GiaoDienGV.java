    public boolean kiemTraMatKhau() throws KiemTraDuLieu
    {
         if(String.valueOf(txtMatKhauCu.getPassword()).equals(""))
       {
            txtMatKhauCu.requestFocus();
           throw new KiemTraDuLieu("Không được để trống mật khẩu cũ!");
       }
          if(String.valueOf(txtMatKhauMoi.getPassword()).equals(""))
       {
            txtMatKhauMoi.requestFocus();
            throw new KiemTraDuLieu("Không được để trống mật khẩu mới!");
       }
           if(String.valueOf(txtXacNhan.getPassword()).equals(""))
       {
            txtXacNhan.requestFocus();
            throw new KiemTraDuLieu("Không được để trống xác nhận mật khẩu!");
       }
          if(!String.valueOf(txtMatKhauCu.getPassword()).equals(gv.getTaiKhoanGV().getMatKhau()))
       {
           txtMatKhauCu.requestFocus();
           throw new KiemTraDuLieu("Mật khẩu cũ không chính xác!");
       }
          if(String.valueOf(txtMatKhauCu.getPassword()).equals(String.valueOf(txtMatKhauMoi.getPassword())))
        {
            txtMatKhauMoi.requestFocus();
            throw new KiemTraDuLieu("Mật khẩu mới trùng với mật khẩu cũ!");
       }
           if(String.valueOf(txtMatKhauMoi.getPassword()).length() < 6)
       {
           txtMatKhauMoi.requestFocus();
           throw new KiemTraDuLieu("Mật khẩu mới phải từ 6 kí tự trở lên!");
       }
            if(!String.valueOf(txtMatKhauMoi.getPassword()).matches("[a-zA-Z0-9]+"))
       {
           txtMatKhauMoi.requestFocus();
            throw new KiemTraDuLieu("Mật khẩu mới không được chứa khoảng trống hoặc kí tự đặc biệt!");
       }
           if(!String.valueOf(txtMatKhauMoi.getPassword()).equals(String.valueOf(txtXacNhan.getPassword())))
        {
            txtXacNhan.requestFocus();
             throw new KiemTraDuLieu("MXác nhận mật khẩu không trùng với mật khẩu mới!");
       }
           return true;
    }
